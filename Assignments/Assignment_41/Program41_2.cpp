////////////////////////////////////////////////////////////////////
//
// Function Name : Sum
// Description   : Returns summation of digits using recursion
// Input         : Integer
// Output        : Integer
// Author        : Sakshi Santosh Jadhav
// Date          : 1/12/2025
//
////////////////////////////////////////////////////////////////////

int Sum(int iNo)
{
    static int iSum = 0;

    if(iNo != 0)
    {
        iSum = iSum + (iNo % 10);
        Sum(iNo / 10);
    }
    return iSum;
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    cout << "Enter number : ";
    cin >> iValue;

    iRet = Sum(iValue);

    cout << iRet;

    return 0;
}

