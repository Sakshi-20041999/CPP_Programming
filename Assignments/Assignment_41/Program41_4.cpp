#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////
//
// Function Name : Fact
// Description   : Returns factorial using recursion
// Input         : Integer
// Output        : Integer
// Author        : Sakshi Santosh Jadhav
// Date          : 1/12/2025
//
////////////////////////////////////////////////////////////////////

int Fact(int iNo)
{
    static int iFact = 1;

    if(iNo > 0)
    {
        iFact = iFact * iNo;
        Fact(iNo - 1);
    }
    return iFact;
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    cout << "Enter number : ";
    cin >> iValue;

    iRet = Fact(iValue);

    cout << iRet;

    return 0;
}

