#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////
//
// Function Name : Strlen
// Description   : Returns length of string using recursion
// Input         : Character Pointer
// Output        : Integer
// Author        : Sakshi Santosh Jadhav
// Date          : 1/12/2025
//
////////////////////////////////////////////////////////////////////

int Strlen(char *str)
{
    static int iCnt = 0;

    if(*str != '\0')
    {
        iCnt++;
        Strlen(str + 1);
    }
    return iCnt;
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    char arr[20];
    int iRet = 0;

    cout << "Enter string : ";
    cin >> arr;

    iRet = Strlen(arr);

    cout << iRet;

    return 0;
}

