#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////
//
// Function Name : Mult
// Description   : Returns product of digits using recursion
// Input         : Integer
// Output        : Integer
// Author        : Sakshi Santosh Jadhav
// Date          : 1/12/2025
//
////////////////////////////////////////////////////////////////////

int Mult(int iNo)
{
    static int iMult = 1;

    if(iNo != 0)
    {
        iMult = iMult * (iNo % 10);
        Mult(iNo / 10);
    }
    return iMult;
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    cout << "Enter number : ";
    cin >> iValue;

    iRet = Mult(iValue);

    cout << iRet;

    return 0;
}

