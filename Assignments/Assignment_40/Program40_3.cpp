#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////
//
// Function Name : Display
// Description   : Displays numbers from N to 1 using recursion
// Input         : Integer
// Output        : 5 4 3 2 1
// Author        : Sakshi Santosh Jadhav
// Date          : 1/12/2025
//
////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    if(iNo > 0)
    {
        cout << iNo << "\t";
        Display(iNo - 1);
    }
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter number : ";
    cin >> iValue;

    Display(iValue);

    return 0;
}

