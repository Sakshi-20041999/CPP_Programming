#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////
//
// Function Name : Display
// Description   : Displays capital letters using recursion
// Input         : Integer
// Output        : A B C D E F
// Author        : Sakshi Santosh Jadhav
// Date          : 1/12/2025
//
////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    static char ch = 'A';

    if(iNo > 0)
    {
        cout << ch << "\t";
        ch++;
        Display(iNo - 1);
    }
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter number : ";
    cin >> iValue;

    Display(iValue);

    return 0;
}

