#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////
//
// Function Name : Display
// Description   : Displays numbers from 1 to N using recursion
// Input         : Integer
// Output        : 1 2 3 4 5
// Author        : Sakshi Santosh Jadhav
// Date          : 1/12/2025
//
////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    static int i = 1;

    if(i <= iNo)
    {
        cout << i << "\t";
        i++;
        Display(iNo);
    }
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter number : ";
    cin >> iValue;

    Display(iValue);

    return 0;
}

