#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////
//
// Function Name : Display
// Description   : Displays numbers from 5 to 1 using recursion
// Input         : None
// Output        : 5   4   3   2   1
// Author        : Sakshi Santosh Jadhav
// Date          : 1/12/2025
//
////////////////////////////////////////////////////////////////////

void Display()
{
    static int i = 5;

    if(i >= 1)
    {
        cout << i << "\t";
        i--;
        Display();
    }
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

