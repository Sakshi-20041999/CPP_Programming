#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////
//
// Function Name : Display
// Description   : Displays capital letters A to F using recursion
// Input         : None
// Output        : A   B   C   D   E   F
// Author        : Sakshi Santosh Jadhav
// Date          : 1/12/2025
//
////////////////////////////////////////////////////////////////////

void Display()
{
    static char ch = 'A';

    if(ch <= 'F')
    {
        cout << ch << "\t";
        ch++;
        Display();
    }
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

