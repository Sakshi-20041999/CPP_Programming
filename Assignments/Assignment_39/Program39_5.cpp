#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////
//
// Function Name : Display
// Description   : Displays small letters a to f using recursion
// Input         : None
// Output        : a   b   c   d   e   f
// Author        : Sakshi Santosh Jadhav
// Date          : 1/12/2025
//
////////////////////////////////////////////////////////////////////

void Display()
{
    static char ch = 'a';

    if(ch <= 'f')
    {
        cout << ch << "\t";
        ch++;
        Display();
    }
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

