#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////
//
// Function Name : Display
// Description   : Displays * 5 times using recursion
// Input         : None
// Output        : *   *   *   *   *
// Author        : Sakshi Santosh Jadhav
// Date          : 1/12/2025
//
////////////////////////////////////////////////////////////////////

void Display()
{
    static int i = 1;

    if(i <= 5)
    {
        cout << "*\t";
        i++;
        Display();
    }
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

