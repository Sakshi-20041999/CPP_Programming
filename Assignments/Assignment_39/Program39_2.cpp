#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////
//
// Function Name : Display
// Description   : Displays numbers from 1 to 5 using recursion
// Input         : None
// Output        : 1   2   3   4   5
// Author        : Sakshi Santosh Jadhav
// Date          : 1/12/2025
//
////////////////////////////////////////////////////////////////////

void Display()
{
    static int i = 1;

    if(i <= 5)
    {
        cout << i << "\t";
        i++;
        Display();
    }
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

